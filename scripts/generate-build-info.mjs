#!/usr/bin/env node

/**
 * Generate build info at build time.
 * Writes lib/build-info.ts with the current commit hash, short hash, year,
 * and build timestamp as compile-time values.
 *
 * Primary: git rev-parse HEAD (works locally and on Pantheon)
 * Fallback: GitHub API
 */

import { execSync } from 'child_process'
import { writeFileSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
const rootDir = join(__dirname, '..')
const outputFile = join(rootDir, 'lib/build-info.ts')

const HEADER = `/**
 * Build information
 * Auto-generated by scripts/generate-build-info.mjs — do not edit manually.
 */

export type BuildInfo = { commitHash: string; commitShort: string; buildTime: string; buildYear: number }
`

async function getCommitFromGitHub() {
  try {
    const response = await fetch(
      'https://api.github.com/repos/jazzsequence/devrel-wait-for-build/commits/main',
      {
        headers: {
          Accept: 'application/vnd.github.v3+json',
          'User-Agent': 'devrel-wait-for-build-build-script',
        },
      }
    )
    if (!response.ok) {
      console.log(`⚠️  GitHub API returned ${response.status}`)
      return null
    }
    const data = await response.json()
    return data.sha ?? null
  } catch (error) {
    console.log('⚠️  Failed to fetch from GitHub API:', error.message)
    return null
  }
}

async function generateBuildInfo() {
  let commitHash

  try {
    commitHash = execSync('git rev-parse HEAD', { cwd: rootDir, encoding: 'utf8' }).trim()
    console.log('✓ Got commit hash from git')
  } catch {
    console.log('⚠️  git command failed, trying GitHub API...')
    commitHash = await getCommitFromGitHub()
  }

  const buildTime = new Date().toISOString()
  const buildYear = new Date().getFullYear()
  const commitShort = commitHash ? commitHash.substring(0, 7) : 'unknown'
  const hash = commitHash ?? 'unknown'

  writeFileSync(
    outputFile,
    `${HEADER}
export const BUILD_INFO: BuildInfo = {
  commitHash: '${hash}',
  commitShort: '${commitShort}',
  buildTime: '${buildTime}',
  buildYear: ${buildYear},
}
`,
    'utf8'
  )

  if (commitHash) {
    console.log(`✅ Build info generated: ${commitShort} (${buildTime})`)
  } else {
    console.warn('⚠️  Build info generated with unknown commit hash')
  }
}

generateBuildInfo().catch((error) => {
  console.error('❌ Fatal error in generate-build-info:', error)
  process.exit(1)
})
